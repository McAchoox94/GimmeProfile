    if (searchText != "") {
      $.ajax({
        url: "search_auto.php",
        method: "post",
        data: {
          query3: searchText,
        },
        success: function (response) {
          $("#show-list3").html(response);
        },
      });
    } else {
      $("#show-list3").html("");
      $("#show-list3").hide();
    }
  });
